# Временная сложность (Time Complexity): 
# (O(N + M)), где (N) — длина массива nums, а (M) — количество запросов в queries. 
# Предобработка (вычисление префиксных сумм) выполняется за один проход по массиву 
# и занимает (O(N)). Обработка каждого из (M) запросов выполняется за константное 
# время (O(1)), так как вычисление суммы на отрезке сводится к одной операции 
# вычитания по индексам.
# Пространственная сложность (Space Complexity): 
# (O(N + M)). Алгоритм требует (O(N)) дополнительной памяти для хранения массива 
# префиксных сумм sums (размером (N + 1)) и (O(M)) памяти для формирования 
# результирующего списка ответов res.

def query_sum(querys, nums):
    sums = [0]
    
    for i in range(len(nums)):
        sums.append(sums[i] + nums[i])

    res = []

    for query in querys:
        res.append(sums[query[1] + 1] - sums[query[0]])

    return res